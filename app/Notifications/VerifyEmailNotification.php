<?php

namespace App\Notifications;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Notifications\Messages\MailMessage;
use Illuminate\Notifications\Notification;
use Illuminate\Support\Facades\URL;

class VerifyEmailNotification extends Notification
{
    use Queueable;

    /**
     * Create a new notification instance.
     */
    public function __construct()
    {
        //
    }

    /**
     * Get the notification's delivery channels.
     *
     * @return array<int, string>
     */
    public function via(object $notifiable): array
    {
        return ['mail'];
    }

    /**
     * Get the mail representation of the notification.
     */
    public function toMail(object $notifiable): MailMessage
    {
        $verificationUrl = URL::temporarySignedRoute(
            'verification.verify',
            now()->addMinutes(60),
            ['id' => $notifiable->getKey(), 'hash' => sha1($notifiable->getEmailForVerification()), 'locale' => $notifiable->locale ?? app()->getLocale()]
        );

        $locale = $notifiable->locale ?? app()->getLocale();
        $userName = $notifiable->first_name ?? $notifiable->name ?? 'User';
        $appName = config('app.name');

        return (new MailMessage)
            ->subject($locale === 'ar' ? 'تحقق من بريدك الإلكتروني - ' . $appName : 'Verify Your Email - ' . $appName)
            ->view('emails.verify-email', [
                'userName' => $userName,
                'verificationUrl' => $verificationUrl,
                'locale' => $locale,
            ]);
    }

    /**
     * Get the array representation of the notification.
     *
     * @return array<string, mixed>
     */
    public function toArray(object $notifiable): array
    {
        return [
            //
        ];
    }
}
